window.translations = translations;
window.currentLang = 'fr'; // default

// Helper de traduction utilisable depuis le JS dynamique
function t(key, fallback) {
    const lang = window.currentLang || 'fr';
    return (translations[lang] && translations[lang][key]) || fallback || key;
}
window.t = t;

function setLanguage(lang) {
    if (!translations[lang]) return;
    window.currentLang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                el.setAttribute('placeholder', translations[lang][key]);
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });
    // Réaffiche les vues dynamiques pour appliquer la langue
    if (typeof window.onLanguageChanged === 'function') window.onLanguageChanged(lang);
}
