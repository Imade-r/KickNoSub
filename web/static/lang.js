const LANG_LOCALES = {
    en: "en-US",
    fr: "fr-FR",
    es: "es-ES",
    pt: "pt-BR"
};

function normalizeLanguage(value) {
    const lang = String(value || "").toLowerCase().slice(0, 2);
    return translations[lang] ? lang : null;
}

function initialLanguage() {
    return normalizeLanguage(localStorage.getItem("ksn_lang")) ||
        normalizeLanguage(navigator.language) ||
        "fr";
}

function setMeta(selector, value) {
    const el = document.querySelector(selector);
    if (el && value) el.setAttribute("content", value);
}

function updateDocumentMeta(lang) {
    const dict = translations[lang];
    document.title = dict.meta_title;
    setMeta('meta[name="description"]', dict.meta_description);
    setMeta('meta[name="keywords"]', dict.meta_keywords);
    setMeta('meta[property="og:title"]', dict.meta_title);
    setMeta('meta[property="og:description"]', dict.meta_description);
    setMeta('meta[name="twitter:title"]', dict.meta_title);
    setMeta('meta[name="twitter:description"]', dict.meta_description);
}

function t(key, fallback) {
    const lang = window.currentLang || "fr";
    return (translations[lang] && translations[lang][key]) || fallback || key;
}

function getCurrentLocale() {
    return LANG_LOCALES[window.currentLang] || LANG_LOCALES.fr;
}

function setLanguage(lang, options = {}) {
    const nextLang = normalizeLanguage(lang);
    if (!nextLang) return;

    window.currentLang = nextLang;
    document.documentElement.lang = nextLang;
    if (options.persist !== false) localStorage.setItem("ksn_lang", nextLang);

    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        const value = translations[nextLang][key];
        if (!value) return;
        if ((el.tagName === "INPUT" || el.tagName === "TEXTAREA") && el.hasAttribute("placeholder")) {
            el.setAttribute("placeholder", value);
        } else {
            el.innerHTML = value;
        }
    });

    document.querySelectorAll(".lang-link").forEach(link => {
        link.classList.toggle("active", link.getAttribute("data-lang") === nextLang);
        link.setAttribute("aria-current", link.getAttribute("data-lang") === nextLang ? "true" : "false");
    });

    updateDocumentMeta(nextLang);
    if (typeof window.onLanguageChanged === "function") window.onLanguageChanged(nextLang);
}

function initLanguage() {
    setLanguage(initialLanguage(), { persist: true });
}

window.translations = translations;
window.currentLang = initialLanguage();
window.t = t;
window.setLanguage = setLanguage;
window.initLanguage = initLanguage;
window.getCurrentLocale = getCurrentLocale;
